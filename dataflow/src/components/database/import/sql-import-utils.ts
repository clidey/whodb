import { ImportFileFormat, type ImportColumnMapping, type ImportFileInput, type ImportFileOptions, type ImportMode, type ImportSqlInput } from '@graphql'

export type SqlScriptSource =
  | {
      kind: 'file'
      file: File
      filename: string
      preview: string
    }
  | {
      kind: 'text'
      script: string
    }

export interface TableFileSource {
  kind: 'tableFile'
  file: File
  filename: string
  format: ImportFileFormat
}

interface BuildImportTableFileInputParams {
  source: TableFileSource
  schema: string
  storageUnit: string
  mode: ImportMode
  mapping: ImportColumnMapping[]
  delimiter: string
  sheet: string
  allowAutoGenerated: boolean
}

/** Returns whether a selected file can be used as a SQL script source. */
export function isAcceptedSqlFile(file: File): boolean {
  return file.name.toLowerCase().endsWith('.sql')
}

/** Returns the import format implied by a supported table file name. */
export function getTableFileFormat(filename: string): ImportFileFormat | null {
  const lowerName = filename.toLowerCase()
  if (lowerName.endsWith('.csv')) return ImportFileFormat.Csv
  if (lowerName.endsWith('.xls') || lowerName.endsWith('.xlsx')) return ImportFileFormat.Excel
  return null
}

/** Returns whether a selected file can be used as a table import source. */
export function isAcceptedTableFile(file: File): boolean {
  return getTableFileFormat(file.name) !== null
}

/** Returns whether the active SQL script source contains executable content. */
export function hasSqlScriptSource(source: SqlScriptSource | null): boolean {
  if (!source) return false
  if (source.kind === 'file') return source.preview.trim().length > 0
  return source.script.trim().length > 0
}

/** Builds parser options for CSV or Excel table imports. */
export function buildTableFileOptions(source: TableFileSource, delimiter: string, sheet: string): ImportFileOptions {
  return {
    Format: source.format,
    ...(source.format === ImportFileFormat.Csv && delimiter ? { Delimiter: delimiter } : {}),
    ...(source.format === ImportFileFormat.Excel && sheet.trim() ? { Sheet: sheet.trim() } : {}),
  }
}

/** Builds the GraphQL input for the active SQL script source. */
export function buildImportSqlInput(source: SqlScriptSource): ImportSqlInput {
  if (source.kind === 'file') {
    return {
      File: source.file,
      Filename: source.filename,
    }
  }

  return {
    Script: source.script,
  }
}

/** Builds the GraphQL input for importing a CSV or Excel file into an existing table. */
export function buildImportTableFileInput({
  source,
  schema,
  storageUnit,
  mode,
  mapping,
  delimiter,
  sheet,
  allowAutoGenerated,
}: BuildImportTableFileInputParams): ImportFileInput {
  return {
    Schema: schema,
    StorageUnit: storageUnit,
    Mode: mode,
    Options: buildTableFileOptions(source, delimiter, sheet),
    File: source.file,
    Mapping: mapping,
    AllowAutoGenerated: allowAutoGenerated,
  }
}
