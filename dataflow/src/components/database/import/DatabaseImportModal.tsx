function getNewTableValidationError(tableName: string, columns: ImportNewTableColumnInput[]): string | null {
  if (!tableName.trim()) return 'import.validation.table_name_empty'
  if (columns.length === 0) return 'import.validation.mapping_invalid'

  const seenTargets = new Set<string>()
  let includedCount = 0

  for (const column of columns) {
    if (column.Skip) {
      if (column.Primary) return 'import.validation.primary_skipped'
      continue
    }

    includedCount += 1
    const targetColumn = column.TargetColumn?.trim() ?? ''
    if (!targetColumn) return 'import.validation.column_name_empty'
    if (seenTargets.has(targetColumn)) return 'import.validation.column_duplicate'
    seenTargets.add(targetColumn)

    if (!column.Type?.trim()) return 'import.validation.column_type_invalid'
    if (column.Primary && column.Nullable) return 'import.validation.primary_nullable'
  }

  if (includedCount === 0) return 'import.validation.all_columns_skipped'
  return null
}

function canRunTableImport(
  tableTargetMode: TableTargetMode,
  tableName: string | null | undefined,
  tableSource: TableFileSource | null,
  tablePreview: TableImportPreview | null,
  allowAutoGenerated: boolean,
  newTableValidationError: string | null,
  metadataLoading: boolean,
  metadataFailed: boolean,
): boolean {
  if (!tableSource || !tablePreview) return false
  if (tablePreview.ValidationError) return false
  if (tableTargetMode === 'new') {
    if (!tablePreview.NewTable) return false
    if (metadataLoading || metadataFailed || newTableValidationError) return false
    return true
  }

  if (!tableName) return false
  if (!tablePreview.Mapping || tablePreview.Mapping.length === 0) return false
  if (tablePreview.RequiresAllowAutoGenerated && !allowAutoGenerated) return false
  return true
}

function DatabaseImportFooter() {
  const { t } = useI18n()
  const {
    method,
    tableTargetMode,
    source,
    result,
    executeImport,
    tableSource,
    tablePreview,
    allowAutoGenerated,
    newTableValidationError,
    metadataLoading,
    metadataFailed,
    previewLoading,
    tableName,
  } = useDatabaseImport()
  const { state } = useModalForm()
  const canRun = method === 'tableFile'
    ? canRunTableImport(
        tableTargetMode,
        tableName,
        tableSource,
        tablePreview,
        allowAutoGenerated,
        newTableValidationError,
        metadataLoading,
        metadataFailed,
      )
    : hasSqlScriptSource(source)

  if (result?.status) {
    return (
      <DialogFooter>
        <DialogClose asChild>
          <Button variant="outline">{t('common.actions.close')}</Button>
        </DialogClose>
      </DialogFooter>
    )
  }

  return (
    <DialogFooter>
      <DialogClose asChild>
        <Button type="button" variant="outline" disabled={state.isSubmitting}>
          {t('common.actions.cancel')}
        </Button>
      </DialogClose>
      <Button type="button" onClick={executeImport} disabled={!canRun || state.isSubmitting || previewLoading} className="gap-2">
        {state.isSubmitting ? <Loader2 className="h-4 w-4 animate-spin" /> : <Upload className="h-4 w-4" />}
        {state.isSubmitting ? t('database.import.running') : t('database.import.runImport')}
      </Button>
    </DialogFooter>
  )
}

