type ImportMethod = 'sql' | 'tableFile'
type SourceKind = 'file' | 'text'
type TableTargetMode = 'existing' | 'new'
type TableImportPreview = ImportPreviewMutation['ImportPreview']

interface DatabaseImportContextValue {
  method: ImportMethod
  sourceKind: SourceKind
  tableTargetMode: TableTargetMode
  source: SqlScriptSource | null
  tableSource: TableFileSource | null
  tablePreview: TableImportPreview | null
  targetDatabase: string
  databaseOptions: string[]
  tableName?: string | null
  targetTableLabel: string
  tableImportMode: ImportMode
  csvDelimiter: string
  excelSheet: string
  useHeaderMapping: boolean
  allowAutoGenerated: boolean
  newTableName: string
  newTableColumns: ImportNewTableColumnInput[]
  typeOptions: ColumnTypeOption[]
  metadataLoading: boolean
  metadataFailed: boolean
  newTableValidationError: string | null
  previewLoading: boolean
  result: ImportResultState | null
  sqlFileInputRef: React.RefObject<HTMLInputElement | null>
  tableFileInputRef: React.RefObject<HTMLInputElement | null>
  setMethod: (method: ImportMethod) => void
  setSourceKind: (kind: SourceKind) => void
  setTableTargetMode: (mode: TableTargetMode) => void
  setTargetDatabase: (database: string) => void
  setTableImportMode: (mode: ImportMode) => void
  setCsvDelimiter: (delimiter: string) => void
  setExcelSheet: (sheet: string) => void
  setUseHeaderMapping: (useHeaderMapping: boolean) => void
  setAllowAutoGenerated: (allowAutoGenerated: boolean) => void
  setNewTableName: (name: string) => void
  updateNewTableColumn: (index: number, field: keyof ImportNewTableColumnInput, value: string | boolean) => void
  handleFileChange: (event: ChangeEvent<HTMLInputElement>) => void
  handleTableFileChange: (event: ChangeEvent<HTMLInputElement>) => void
  handleScriptChange: (script: string) => void
  convertFileToText: () => void
  loadTablePreview: () => Promise<void>
  executeImport: () => Promise<void>
}

interface ImportResultState {
  status: boolean
  detail?: string | null
  message?: string | null
}

interface DatabaseImportProviderProps {
  connectionId: string
  databaseName: string
  schema?: string | null
  tableName?: string | null
  onSuccess?: (context: { databaseName: string; schema?: string | null; tableName?: string | null }) => void
  children: ReactNode
}

