function TableImportPreview() {
  const { t } = useI18n()
  const { tableTargetMode, tablePreview, previewLoading } = useDatabaseImport()

  if (previewLoading) {
    return (
      <div role="status" className="flex items-center gap-2 rounded-md border border-primary/20 bg-primary/5 p-3 text-sm text-primary">
        <Loader2 className="h-4 w-4 animate-spin" />
        <span>{t('database.import.table.previewLoading')}</span>
      </div>
    )
  }

  if (!tablePreview) {
    return null
  }

  if (tablePreview.ValidationError) {
    return (
      <div role="alert" className="flex items-start gap-2 rounded-md border border-destructive/20 bg-destructive/5 p-3 text-sm text-destructive">
        <AlertTriangle className="mt-0.5 h-4 w-4 shrink-0" />
        <span>{formatImportFailure(t, tablePreview.ValidationError, true)}</span>
      </div>
    )
  }

  return (
    <div className="flex flex-col gap-3">
      {tableTargetMode === 'existing' && tablePreview.Mapping && tablePreview.Mapping.length > 0 && (
        <div className="flex flex-col gap-2">
          <span className="text-sm font-medium text-foreground">{t('database.import.table.mapping')}</span>
          <div className="max-h-40 overflow-auto rounded-md border">
            <table className="w-full min-w-96 text-left text-xs">
              <thead className="bg-muted/50 text-muted-foreground">
                <tr>
                  <th className="px-3 py-2 font-medium">{t('database.import.table.sourceColumn')}</th>
                  <th className="px-3 py-2 font-medium">{t('database.import.table.targetColumn')}</th>
                </tr>
              </thead>
              <tbody>
                {tablePreview.Mapping.map((mapping) => (
                  <tr key={`${mapping.SourceColumn}-${mapping.TargetColumn}`} className="border-t">
                    <td className="px-3 py-2 font-mono">{mapping.SourceColumn}</td>
                    <td className="px-3 py-2 font-mono">{mapping.TargetColumn}</td>
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </div>
      )}

      {tableTargetMode === 'new' && tablePreview.NewTable && <NewTableDefinitionEditor />}

      <div className="flex flex-col gap-2">
        <span className="text-sm font-medium text-foreground">{t('database.import.table.previewRows')}</span>
        <div className="max-h-56 overflow-auto rounded-md border">
          <table className="w-full min-w-128 text-left text-xs">
            <thead className="bg-muted/50 text-muted-foreground">
              <tr>
                {tablePreview.Columns.map((column) => (
                  <th key={column} className="px-3 py-2 font-medium">{column}</th>
                ))}
              </tr>
            </thead>
            <tbody>
              {tablePreview.Rows.map((row, rowIndex) => (
                <tr key={rowIndex} className="border-t">
                  {tablePreview.Columns.map((column, columnIndex) => (
                    <td key={`${rowIndex}-${column}`} className="px-3 py-2 font-mono">
                      {row[columnIndex] ?? ''}
                    </td>
                  ))}
                </tr>
              ))}
            </tbody>
          </table>
        </div>
        {tablePreview.Truncated && (
          <span className="text-xs text-muted-foreground">{t('database.import.table.truncated')}</span>
        )}
      </div>
    </div>
  )
}

